class Node
{
public:
    int data;
    Node *left;
    Node *right;

    Node(int val)
    {
        data = val;
        left = right = nullptr;
    }
};

int findMaxFork(Node *root, int k)
{
    // code here
    int ans = INT_MIN;

    while (root)
    {
        if (root->data <= k)
        {
            ans = max(ans, root->data);
            root = root->right;
        }
        else
            root = root->left;
    }

    return ans;
}

